export function t(key: string, lang: Lang, vars?: Record<string, string | number>): string {
  let s = strings[lang]?.[key] ?? strings.en[key] ?? key;
  if (vars) {
    for (const k in vars) {
      s = s.replace(`{${k}}`, String(vars[k]));
    }
  }
  return s;
}

export function isRTL(lang: Lang): boolean {
  return lang === 'he';
}

export async function loadSavedLang(): Promise<Lang> {
  try {
    const saved = await AsyncStorage.getItem('afkaf_lang');
    if (saved && (saved === 'he' || saved === 'en' || saved === 'ru')) return saved;
  } catch {}
  return 'he'; // Hebrew default per spec
}

export async function saveLang(lang: Lang): Promise<void> {
  await AsyncStorage.setItem('afkaf_lang', lang);
}

export function applyRTL(lang: Lang): void {
  const rtl = isRTL(lang);
  if (I18nManager.isRTL !== rtl) {
    I18nManager.forceRTL(rtl);
  }
}
